                                    <h6 class="mt-40">ระบบสระว่ายน้ำ</h6>

                                    <p><b>ระบบ Overflow </b>คือน้ำจะไหลจากสระว่ายน้ำมาลงที่รางน้ำขอบสระ จากนั้นไหลลงสู่ Surge Tank เข้ามาผ่านถังกรอง แล้วจึงไหลกลับเข้าไปในสระว่ายน้ำอย่างเดิม</p>
                                    <ul>
                                        <li><span>01</span> มีรางน้ำ (Grating) อยู่รอบขอบสระว่ายน้ำ หรือกำหนดให้น้ำล้นออกฝั่งไหนก็ได้ขึ้นอยู่กับการออกแบบ</li>
                                        <li><span>02</span> ระดับน้ำจะอยู่ปริ่มขอบสระว่ายน้ำพอดี ทำให้ผิวน้ำแลดูตึงสวยสะอาดอยู่ตลอดเวลา</li>
                                        <li><span>03</span> มีบ่อพักน้ำ (Surge Tank) มีขนาดไม่น้อยกว่า 10% ของสระว่ายน้ำ</li>
                                        <li><span>04</span> รูปแบบสระดูสวยงามกว่าระบบ Skimmer</li>
                                    </ul>

