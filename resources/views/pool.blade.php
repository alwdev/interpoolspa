                                    <h6 class="mt-40">ระบบฆ่าเชื้อโรค</h6>

                                    <p><b>ระบบคลอรีน </b>คือ การนำคลอรีนในรูปของเหลว ผง เกล็ด หรือแบบก้อน 90กรัม, 15กรัม โดยคลอรีนจะเป็นตัวฆ่าเชื้อโรค และน้ำในสระว่ายน้ำควรมีค่า PH ระหว่าง 7.2-7.8 เพื่อจะทำให้การฆ่าเชื้อโรคมีประสิทธิภาพสูงสุด</p>
      
                                    <p><b>ระบบเกลือ </b>คือ ระบบที่ใช้เกลือธรรมชาติ (NaCL) มาผ่านกระบวนการ Electrotytic Process โดยเครื่อง Salt Clorinator ทำให้กลายเป็นคลอรีนเหลว หรือโซเดียมไฮโปคอลไรด์ และไฮโดรเจน ซึ่งจะช่วยฆ่าเชื้อโรค และนำในสระจะไม่มีคลอรีน ทำให้ไม่แสบจมูก แสบตา หรือระคายเคืองต่อผิวหนังแถมยังเพิ่มความชุ่มชื่นให้แก่ผิวหนัง</p>

                                    <p><b>ระบบโอโซน  </b>คือ กรรมวิธีการคายประจุไฟฟ้า โดยจะเกิดอิเล็กตรอน ซึ่งตัวอิเล็กตรอนนี้จะกระทำปฏิกิริยากับอณูของออกซิเจน ทำให้เกิดปราณูและอณูของออกซิเจนขึ้น ทำให้เกิดโอโซน และระบบนี้ต้องใช้ควบคู่กับระบบคลอรีน หรือระบบเกลือ เพื่อให้ได้ค่าน้ำที่เป็นมาตรฐานกำหนด</p>
                  
